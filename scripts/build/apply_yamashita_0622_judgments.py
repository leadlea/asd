def main():
    with open(PATH, encoding="utf-8") as f:
        text = f.read()
    orig = text

    errors = []
    for old, new in UNIQUE:
        c = text.count(old)
        if c != 1:
            errors.append(f"[UNIQUE count={c}] {old[:60]!r}")
            continue
        text = text.replace(old, new)
    for old, new in MULTI:
        c = text.count(old)
        if c < 1:
            errors.append(f"[MULTI count=0] {old[:60]!r}")
            continue
        text = text.replace(old, new)

    if errors:
        print("=== 置換失敗（適用中止） ===")
        for e in errors:
            print(e)
        sys.exit(1)

    # 残存チェック: bare「アンサンブル」が消えたか
    remaining = text.count("アンサンブル")
    print(f"残存『アンサンブル』: {remaining}")
    remaining_hq1 = text.count("品質フィルタ") + text.count("HQ1フィルタ")
    print(f"残存『品質フィルタ/HQ1フィルタ』: {remaining_hq1}")

    with open(PATH, "w", encoding="utf-8") as f:
        f.write(text)
    print(f"適用完了: {len(UNIQUE)} unique + {len(MULTI)} multi 置換 / {len(text)-len(orig):+d} chars")


if __name__ == "__main__":
    main()
